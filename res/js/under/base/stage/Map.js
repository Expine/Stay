/**
 * Map
 * Manage map element such as background
 * @classdesc Map class for managing map element
 */
class Map {
    /**
     * Constructor for map
     * @constructor
     * @param {number} width Map width per pixel
     * @param {number} height Map height per pixel
     */
    constructor(width, height) {
        /**
         * Map width
         * @type {number}
         */
        this.width = width;
        /**
         * Map height
         * @type {number}
         */
        this.height = height;
    }
}